package ch14;

public class Student2 {
	private String num;
	private String name;
	private String cla;
	private int point;
	public Student2() {
	}
	public Student2(String num, String name, String cla, int point) {
		this.num = num;
		this.name = name;
		this.cla = cla;
		this.point = point;
	}
	public String getNum() {
		return num;
	}
	public void setNum(String num) {
		this.num = num;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getCla() {
		return cla;
	}
	public void setCla(String cla) {
		this.cla = cla;
	}
	public int getPoint() {
		return point;
	}
	public void setPoint(int point) {
		this.point = point;
	}
	
	
	
	
	
	

}
