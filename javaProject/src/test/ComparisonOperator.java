package test;

public class ComparisonOperator {

	public static void main(String[] args) {
		int x= 3;
		int y = 4;
		System.out.println(x==y ); //x와 y가 같은가?
		System.out.println(x!=y); // x와 y가 다른가?
		System.out.println(x>y); //x가 y보다 큰가?
		System.out.println(x<y); //x가 y보다 작은가?
	}

}
